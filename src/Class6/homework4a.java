package Class6;

public class homework4a {
    public static void main(String[] args) {

        /*
        Print even numbers from 20 to 1 (2 ways)  === The first way

         */
        int i = 20;

        while (i >= 1) {
            if (i % 2 == 0) {
                System.out.print(i + " ");
            }

            i--;
        }
    }
}
